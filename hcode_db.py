def get_hazard_statements(h_codes: list) -> str:
    """
    H코드 리스트를 받아서 유해위험문구 반환
    """
    lines = []
    seen = set()

    for code in h_codes:
        code = code.strip().upper()
        if code in HCODE_DB and code not in seen:
            lines.append(f"{code} {HCODE_DB[code]}")
            seen.add(code)

    return "\n".join(lines)
